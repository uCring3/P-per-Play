	if general:aabb(self.cambia_turno.x, self.cambia_turno.y, self.cambia_turno.width, self.cambia_turno.height, mouse.x, mouse.y, 1, 1) and giocatore == 1 then
		if hoster then
			Abilita:cambiaTurno()
			server:sendToAll("cambia_turno_1-2", true)
		elseif connesso then
			Abilita:cambiaTurno()
			client:send("cambia_turno_2-1", true)
		else
			Abilita:cambiaTurno()
		end
	end
end


