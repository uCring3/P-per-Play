function room:loadAssets()
	self.background = love.graphics.newImage("assets/background.png")
	self.cambia_turno = {}
	self.cambia_turno.img = love.graphics.newImage("assets/cambia_turno.png")
	self.cambia_turno.x = (window.width)-230
	self.cambia_turno.y = (window.height/2)-120
	self.cambia_turno.width = self.cambia_turno.img:getWidth()
	self.cambia_turno.height = self.cambia_turno.img:getHeight()
end


